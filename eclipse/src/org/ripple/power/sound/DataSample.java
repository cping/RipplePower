package org.ripple.power.sound;


/**
 * DataSample
 */
public final class DataSample {
	protected long offset;
	protected int size;
	
	public DataSample(long offset, int size) {
		super();
		this.offset = offset;
		this.size = size;
	}

	public long getOffset() {
		return offset;
	}

	public int getSize() {
		return size;
	}

}
